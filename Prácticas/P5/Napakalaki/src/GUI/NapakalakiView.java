    private void siguienteTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_siguienteTActionPerformed
        // TODO add your handling code here:
        if( this.napakalakiModel.nextTurn() ){        
            this.resultCombat.setText("Turno de " + this.napakalakiModel.getCurrentPlayer().getName());
            this.setNapakalaki(napakalakiModel);
            this.verMonstruo.setEnabled(true);
        }
        else
        {            
            this.resultCombat.setText("No has cumplido el mal rollo o tienes mas de 4 tesoros ocultos");
            this.setNapakalaki(napakalakiModel);
            this.siguienteT.setEnabled(true);
            this.verMonstruo.setEnabled(false);            
        }        
    }//GEN-LAST:event_siguienteTActionPerformed

