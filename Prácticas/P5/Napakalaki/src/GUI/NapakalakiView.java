    private void combateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_combateActionPerformed
        // TODO add your handling code here:
        this.combate.setEnabled(false);       
        CombatResult result = napakalakiModel.developCombat();        
        String resultado = null;
        switch (result) {
            case WinAndWinGame : 
              resultado = "\n\n ¡¡¡ H A S   G A N A D O   L A   P A R T I D A !!!";
              break;
            case Win :
              resultado = "\n\n Ganaste el combate";
              break;
            case Lose :
              resultado = "\n\n Has perdido el combate, te toca cumplir el mal rollo";
              break;
            case LoseAndEscape :
              resultado = "\n\n Perdiste el combate pero has logrado escapar";
              break;
            case LoseAndDie :
              resultado = "\n\n Perdiste el combate y además estás muerto";
              break;
            case LoseAndConvert :
              resultado = "\n\n Ahora eres un SECTARIO.";
              break;
        }
        this.resultCombat.setText(resultado);
        this.setNapakalaki(napakalakiModel);
        this.verMonstruo.setEnabled(false);
        this.siguienteT.setEnabled(true);
    }//GEN-LAST:event_combateActionPerformed

